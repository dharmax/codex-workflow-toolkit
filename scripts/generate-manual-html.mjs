#!/usr/bin/env node

import path from "node:path";
import { createHash } from "node:crypto";
import { readFile, writeFile } from "node:fs/promises";
import { fileURLToPath } from "node:url";
import { ensureDir } from "../runtime/scripts/ai-workflow/lib/fs-utils.mjs";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, "..");

export async function generateManualHtml({
  root = repoRoot,
  source = path.resolve(root, "docs", "MANUAL.md"),
  output = path.resolve(root, "docs", "manual.html")
} = {}) {
  const markdown = await readFile(source, "utf8");
  const html = renderManualHtml(markdown, { sourcePath: path.relative(root, source).replace(/\\/g, "/") });
  await ensureDir(path.dirname(output));
  await writeFile(output, html, "utf8");
  return {
    source,
    output,
    html
  };
}

export function renderManualHtml(markdown, { sourcePath = "docs/MANUAL.md" } = {}) {
  const tokens = tokenizeMarkdown(markdown);
  const sourceHash = createHash("sha1").update(String(markdown)).digest("hex");
  const title = tokens.find((token) => token.type === "heading" && token.level === 1)?.text ?? "ai-workflow Manual";
  const toc = tokens.filter((token) => token.type === "heading" && token.level >= 2 && token.level <= 3);
  const body = renderTokenBody(tokens);

  return [
    "<!doctype html>",
    '<html lang="en">',
    "<head>",
    '  <meta charset="utf-8">',
    '  <meta name="viewport" content="width=device-width, initial-scale=1">',
    `  <title>${escapeHtml(title)}</title>`,
    `  <meta name="generator" content="ai-workflow docs:manual">`,
    `  <meta name="manual-source" content="${escapeHtml(sourcePath)}">`,
    `  <meta name="manual-source-sha1" content="${sourceHash}">`,
    "</head>",
    "<body>",
    `  <!-- generated from ${escapeHtml(sourcePath)} sha1:${sourceHash} -->`,
    "  <header>",
    `    <h1>${escapeHtml(title)}</h1>`,
    '    <p>This HTML manual is generated from the canonical Markdown manual.</p>',
    `    <p><a href="${escapeHtml(path.posix.basename(sourcePath))}">Canonical Markdown source</a></p>`,
    "  </header>",
    "  <main>",
    "    <nav aria-labelledby=\"manual-toc-heading\">",
    "      <h2 id=\"manual-toc-heading\">Table of contents</h2>",
    "      <ol>",
    ...toc.map((item) => `        <li><a href="#${escapeHtml(item.id)}">${escapeHtml(item.text)}</a></li>`),
    "      </ol>",
    "    </nav>",
    "    <article>",
    ...body.map((line) => `      ${line}`),
    "    </article>",
    "  </main>",
    "  <footer>",
    "    <p>Generated by <code>pnpm docs:manual</code>.</p>",
    "  </footer>",
    "</body>",
    "</html>",
    ""
  ].join("\n");
}

function renderTokenBody(tokens) {
  const lines = [];
  const sectionStack = [];
  let sawH1 = false;

  for (const token of tokens) {
    if (token.type === "heading") {
      if (token.level === 1) {
        sawH1 = true;
        continue;
      }
      const sectionLevel = Math.min(token.level, 6);
      while (sectionStack.length && sectionStack.at(-1) >= sectionLevel) {
        lines.push("</section>");
        sectionStack.pop();
      }
      lines.push(`<section aria-labelledby="${escapeHtml(token.id)}" data-heading-level="${sectionLevel}">`);
      lines.push(`<h${sectionLevel} id="${escapeHtml(token.id)}">${renderInline(token.text)}</h${sectionLevel}>`);
      sectionStack.push(sectionLevel);
      continue;
    }

    if (!sawH1) {
      sawH1 = true;
    }

    if (token.type === "paragraph") {
      lines.push(`<p>${renderInline(token.text)}</p>`);
      continue;
    }

    if (token.type === "list") {
      const tag = token.ordered ? "ol" : "ul";
      const attrs = token.ordered ? ' data-list-kind="ordered"' : ' data-list-kind="unordered"';
      lines.push(`<${tag}${attrs}>`);
      for (const item of token.items) {
        lines.push(`<li>${renderInline(item)}</li>`);
      }
      lines.push(`</${tag}>`);
      continue;
    }

    if (token.type === "code") {
      const langAttr = token.lang ? ` class="language-${escapeHtml(token.lang)}"` : "";
      lines.push(`<pre data-block-kind="code"><code${langAttr}>${escapeHtml(token.text)}</code></pre>`);
    }
  }

  while (sectionStack.length) {
    lines.push("</section>");
    sectionStack.pop();
  }

  return lines;
}

function tokenizeMarkdown(markdown) {
  const lines = String(markdown).replace(/\r\n/g, "\n").split("\n");
  const tokens = [];
  let index = 0;

  while (index < lines.length) {
    const line = lines[index];
    const trimmed = line.trim();

    if (!trimmed) {
      index += 1;
      continue;
    }

    const headingMatch = trimmed.match(/^(#{1,6})\s+(.+)$/);
    if (headingMatch) {
      const text = headingMatch[2].trim();
      tokens.push({
        type: "heading",
        level: headingMatch[1].length,
        text,
        id: slugify(text)
      });
      index += 1;
      continue;
    }

    const codeFenceMatch = trimmed.match(/^```(\S*)\s*$/);
    if (codeFenceMatch) {
      const lang = codeFenceMatch[1] || "";
      const block = [];
      index += 1;
      while (index < lines.length && !lines[index].trim().startsWith("```")) {
        block.push(lines[index]);
        index += 1;
      }
      if (index < lines.length) {
        index += 1;
      }
      tokens.push({
        type: "code",
        lang,
        text: block.join("\n")
      });
      continue;
    }

    const bulletMatch = trimmed.match(/^[-*]\s+(.+)$/);
    const orderedMatch = trimmed.match(/^\d+\.\s+(.+)$/);
    if (bulletMatch || orderedMatch) {
      const ordered = Boolean(orderedMatch);
      const items = [];
      while (index < lines.length) {
        const current = lines[index].trim();
        const listMatch = ordered
          ? current.match(/^\d+\.\s+(.+)$/)
          : current.match(/^[-*]\s+(.+)$/);
        if (!listMatch) {
          break;
        }
        items.push(listMatch[1].trim());
        index += 1;
      }
      tokens.push({
        type: "list",
        ordered,
        items
      });
      continue;
    }

    const paragraph = [];
    while (index < lines.length) {
      const current = lines[index].trim();
      if (!current || /^#{1,6}\s+/.test(current) || /^```/.test(current) || /^[-*]\s+/.test(current) || /^\d+\.\s+/.test(current)) {
        break;
      }
      paragraph.push(current);
      index += 1;
    }
    tokens.push({
      type: "paragraph",
      text: paragraph.join(" ")
    });
  }

  return tokens;
}

function renderInline(text) {
  let result = escapeHtml(String(text ?? ""));
  result = result.replace(/`([^`]+)`/g, "<code>$1</code>");
  result = result.replace(/\*\*([^*]+)\*\*/g, "<strong>$1</strong>");
  result = result.replace(/\[([^\]]+)\]\(([^)]+)\)/g, '<a href="$2">$1</a>');
  return result;
}

function slugify(value) {
  return String(value ?? "")
    .toLowerCase()
    .replace(/[`*_]/g, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-+|-+$/g, "") || "section";
}

function escapeHtml(value) {
  return String(value ?? "")
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

if (process.argv[1] && path.resolve(process.argv[1]) === fileURLToPath(import.meta.url)) {
  const root = process.cwd();
  const result = await generateManualHtml({ root });
  process.stdout.write(`${path.relative(root, result.output).replace(/\\/g, "/")}\n`);
}
